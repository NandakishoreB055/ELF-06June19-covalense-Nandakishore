package com.covalense.java.examples;

public class language {

	public static void main(String[] args) {
		int t = 1;
		switch (t) {
		case 1:
			System.out.println("kannada");
			break;
		case 2:
			System.out.println("hindi");
			break;
		case 3:
			System.out.println("english");
			break;
		default:
			System.out.println("invalid entry");
		}
	}
}
