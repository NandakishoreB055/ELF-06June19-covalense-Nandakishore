package com.covalense.java.examples;

public class grade {

	public static void main(String[] args) {
		int a = 70;
		if (a >= 60) {
			System.out.println("A grade");
		} else if (a >= 50) {
			System.out.println("B grade");
		} else if (a >= 40) {
			System.out.println("C grade");
		} else {
			System.out.println("fail");
		}
	}

}
